    private void btnEliAseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliAseActionPerformed
        // TODO add your handling code here:
       
        ConexionMySQL mysql = new ConexionMySQL();
        Connection cn = mysql.Conectar();
        int fila= tblEliAse.getSelectedRow();
        if(fila<0)
            {
             JOptionPane.showMessageDialog(this, "Seleccione alguna fila");
            }
        else
        {
            int Pregunta;
            Pregunta = JOptionPane.showConfirmDialog(null,"Desea Eliminar Los Datos","Pregunta" ,0);
            if (Pregunta==0)
            {
                idfila= tblEliAse.getValueAt(fila, 0).toString();
                String eli="DELETE FROM aseguradora WHERE cod_emp = '"+idfila+"'";
                PreparedStatement pst;
                try
                {
                    pst = cn.prepareStatement(eli);
                    int m=pst.executeUpdate();
                    if(m>0)
                    {
                        JOptionPane.showMessageDialog(this, "ELIMINO LOS DATOS DE FORMA CORRECTA");
                        String Informacion = lblIconoEliAse.getText();
                        CargarTabla(Informacion);
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(this, "No se pudo eliminar");
                        String Informacion = lblIconoEliAse.getText();
                        CargarTabla(Informacion);
                    }
                
                } catch (SQLException ex) 
                {
                    Logger.getLogger(EliProducto.class.getName()).log(Level.SEVERE, null, ex);
                }
                 try
                    {
                        String sSQL="";

                        sSQL = "DELETE FROM proforma WHERE cod_emp = '"+idfila+"'";
                        Statement st = cn.createStatement();
                        st.execute(sSQL);
                    }
                    catch(Exception e)
                    {
                        JOptionPane.showMessageDialog(null, e);
                    }
            }
        }
    }//GEN-LAST:event_btnEliAseActionPerformed

