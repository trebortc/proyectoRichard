    void CargarTabla(String datos)
    {
        String[] Titulos = {"CÓDIGO" ,"NOMBRE", "CIUDAD", "DIRECCIÓN", "GERENTE","TELÉFONO", "E-MAIL"};
        String[] Informacion = new String[7];

        String sSQL="";

        tabla = new DefaultTableModel(null,Titulos);

        ConexionMySQL mysql = new ConexionMySQL();
        Connection cn = mysql.Conectar();

        sSQL = "SELECT cod_emp, nom_emp, ciu, dir, ger, telf, mail FROM aseguradora " +
                "WHERE CONCAT(cod_emp) LIKE '%"+datos+"%'";

        try
        {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sSQL);

            while(rs.next())
            {
                Informacion[0] = rs.getString("cod_emp");
                Informacion[1] = rs.getString("nom_emp");
                Informacion[2] = rs.getString("ciu");
                Informacion[3] = rs.getString("dir");
                Informacion[4] = rs.getString("ger");
                Informacion[5] = rs.getString("telf");
                Informacion[6] = rs.getString("mail");

                tabla.addRow(Informacion);
            }
            tblEliAse.setModel(tabla);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }

    }
    
