    private void btnExpConProActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExpConProActionPerformed
        //generarExcel();
        try{
            Thread t=new Thread(){
                public void run (){

                HSSFWorkbook workbook = new HSSFWorkbook();
                HSSFSheet hoja = workbook.createSheet();
                CellStyle style = workbook.createCellStyle();
                style.setFillForegroundColor(IndexedColors.LIGHT_TURQUOISE.getIndex());
                style.setFillPattern(CellStyle.SOLID_FOREGROUND);
                
                style.setBorderTop(CellStyle.BORDER_THIN);
                style.setBorderLeft(CellStyle.BORDER_THIN);
            	style.setBorderRight(CellStyle.BORDER_THIN);
		style.setBorderBottom(CellStyle.BORDER_THIN);
                   
                //style.
                String[] titulos = {"CÉDULA" ,"PLACA", "TIPO", "MARCA", "MODELO",
                            "AÑO", "COLOR","VALOR ACTUAL"};
                HSSFRow fila = hoja.createRow(0);
                 // Creamos el encabezado
                for(int i = 0; i < titulos.length; i++) {
                 // Creamos una celda en esa fila, en la posicion 
                 // indicada por el contador del ciclo
                Cell celda = fila.createCell(i);
                celda.setCellStyle(style); 
                celda.setCellValue(titulos[i]);
                }
                    
                    HSSFRow filas;
                    Rectangle rect;
                    for(int i=0; i<tblExpConPro.getRowCount();i++){
                        rect = tblExpConPro.getCellRect(i, 0, true);
                        try
                        {
                            tblExpConPro.scrollRectToVisible(rect);
                        }catch(java.lang.ClassCastException e){}
                        tblExpConPro.setRowSelectionInterval(i, i);
                        
                        filas = hoja.createRow((i+1));
                                                
                        filas.createCell(0).setCellValue(tblExpConPro.getValueAt(i, 0).toString());
                        filas.createCell(1).setCellValue(tblExpConPro.getValueAt(i, 1).toString());
                        filas.createCell(2).setCellValue(tblExpConPro.getValueAt(i, 2).toString());
                        filas.createCell(3).setCellValue(tblExpConPro.getValueAt(i, 3).toString());
                        filas.createCell(4).setCellValue(tblExpConPro.getValueAt(i, 4).toString());
                        filas.createCell(5).setCellValue(tblExpConPro.getValueAt(i, 5).toString());
                        filas.createCell(6).setCellValue(tblExpConPro.getValueAt(i, 6).toString());
                        filas.createCell(7).setCellValue(tblExpConPro.getValueAt(i, 7).toString());
                        
                    }
                    JFileChooser fileChooser = new JFileChooser();
                    FileNameExtensionFilter filter = new FileNameExtensionFilter("Archivos Excel (*.xls)", "xls");
                    fileChooser.setAcceptAllFileFilterUsed(false);
                    fileChooser.setFileFilter(filter);
                    fileChooser.setDialogTitle("GUARDAR ARCHIVO");
                    if (fileChooser.showSaveDialog(null) == JFileChooser.APPROVE_OPTION)
        
                    {

                        try {
                            File archivo = new File(fileChooser.getSelectedFile().getAbsolutePath());

                            OutputStream out = null;
                            if (getFileExtension(archivo)) {
                                out = new FileOutputStream(fileChooser.getSelectedFile().getAbsolutePath());
                            } else {
                                out = new FileOutputStream(fileChooser.getSelectedFile().getAbsolutePath() + ".xls");
                            }

                            JOptionPane.showMessageDialog(null, "Archivo generado con éxito");

                            workbook.write(out);
                            workbook.close();
                            out.flush();
                            out.close();
                        } catch (IOException ex) {
                            Logger.getLogger(ConSolicitud.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                }

        
            };
            t.start();
        }catch(Exception e){JOptionPane.showMessageDialog(null, e);}
        
    }//GEN-LAST:event_btnExpConProActionPerformed

