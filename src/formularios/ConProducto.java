    public void filtros() {
        int columnaABuscar = 0;
  
        if (cboExpConPro.getSelectedItem() == "Cédula") {
            columnaABuscar = 0;
        }
        if (cboExpConPro.getSelectedItem() == "Tipo") {
            columnaABuscar = 2;
        }
        if (cboExpConPro.getSelectedItem().toString() == "Marca") {
            columnaABuscar = 3;
        }
        if (cboExpConPro.getSelectedItem().toString() == "Modelo") {
            columnaABuscar = 4;
        }
        if (cboExpConPro.getSelectedItem() == "Año") {
            columnaABuscar = 5;
        }
        tr.setRowFilter(RowFilter.regexFilter("(?i)"+txtExpConPro.getText(), columnaABuscar));
    
      }
    
    public void setListar(Object Informacion) {
        DefaultTableModel modelo = (DefaultTableModel) this.tblExpConPro.getModel();
        

        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }
        String sql = "SELECT * FROM vehiculo";
        Object Datos[]= new Object[11];
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                Datos[0] = rs.getString("nom_pro");
                Datos[1] = rs.getString("matri_vehi");
                Datos[2] = rs.getString("tip");
                Datos[3] = rs.getString("mar");
                Datos[4] = rs.getString("modelo");
                Datos[5] = rs.getString("anio");
                Datos[6] = rs.getString("col");
                Datos[7] = rs.getString("val");
                modelo.addRow(Datos);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ConSolicitud.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
   
     private boolean getFileExtension(File file) {
        String ext = null;
        String s = file.getName();
        int i = s.lastIndexOf('.');

        if (i > 0 && i < s.length() - 1) {
            ext = s.substring(i + 1).toLowerCase();
        }

        if (ext != null) {
            return true;
        } else {
            return false;
        }
    }


